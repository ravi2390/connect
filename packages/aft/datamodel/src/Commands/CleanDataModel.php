<?php

namespace Aft\DataModel\Commands;

use Aft\DataModel\DataModel;
use Aft\DataModel\DataModelOutput;
use Illuminate\Console\Command;

class CleanDataModel extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'aftdb:clean';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Remove all files generated by this package';

    /**
     * Create a new command instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * @var DataModel
     */
    private $dataModel;

    /**
     * Execute the console command.
     *
     * @return mixed
     *
     * @throws \Exception
     */
    public function handle(): null
    {
        $cacheFile = config('datamodel.cache.path').'/dmcache.json';
        if (file_exists($cacheFile)) {
            unlink($cacheFile);
        }
        $dmo = new DataModelOutput();
        $dmo->cleanDirectories();

        return null;
    }
}
