     * Sets preferred_viewer
     *
     * @param string|null $preferred_viewer The preferred route for viewing this dashboard (ie: dashboards or dashboards-next)
     *
     * @return self
     */
    public function setPreferredViewer($preferred_viewer): static
    {
        if (is_null($preferred_viewer)) {
            array_push($this->openAPINullablesSetToNull, 'preferred_viewer');
        } else {
            $nullablesSetToNull = $this->getOpenAPINullablesSetToNull();
            $index = array_search('preferred_viewer', $nullablesSetToNull);
            if ($index !== false) {
                unset($nullablesSetToNull[$index]);
                $this->setOpenAPINullablesSetToNull($nullablesSetToNull);
            }
        }
        $this->container['preferred_viewer'] = $preferred_viewer;

        return $this;
    }
